package com.example.mytest;

public class ListCommentsEvaluation {
    private String comment;
    private String rate;

    public ListCommentsEvaluation(String comment, String rate) {
        this.comment = comment;
        this.rate = rate;
    }

    public String getComment() {
        return comment;
    }

    public String getRate() {
        return rate;
    }
}
